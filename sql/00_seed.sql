-- Рейтинги: микс шкал 1-5 и 0-100 (в витрине есть нормализация >5 / 10).
INSERT INTO ds_ecom.order_reviews (order_id, review_score)
SELECT
    o.order_id,
    CASE
        WHEN o.order_id % 5 = 0 THEN 92
        WHEN o.order_id % 4 = 0 THEN 4
        WHEN o.order_id % 3 = 0 THEN 5
        ELSE 83
    END::NUMERIC(6, 2) AS review_score
FROM ds_ecom.orders o
WHERE o.order_status = 'Доставлено'
  AND o.order_id NOT IN (1028, 1038, 1044);
