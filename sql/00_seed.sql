INSERT INTO ds_ecom.order_items (order_id, product_id, price, delivery_cost)
SELECT
    o.order_id,
    2000 + (o.order_id % 17) AS product_id,
    CASE
        WHEN o.order_status = 'Доставлено' THEN 1200 + (o.order_id % 9) * 550
        ELSE 900 + (o.order_id % 5) * 250
    END::NUMERIC(12, 2) AS price,
    CASE WHEN o.order_id % 4 = 0 THEN 199 ELSE 299 END::NUMERIC(12, 2) AS delivery_cost
FROM ds_ecom.orders o;

